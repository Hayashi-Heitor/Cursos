numero1 = int(input("Informe o primeiro número: "))
numero2 = int(input("Informe o segundo número: "))

# Soma
soma = numero1 + numero2
print(f"\nSoma dos números: {soma}")

# Subtração
sub = numero1 - numero2
print(f"Sutração dos números: {sub}")

# Multiplicação
multi = numero1 * numero2
print(f"Multiplicação dos números: {multi}")

# Divisão
div = numero1 / numero2
print(f"Divisão dos números: {div}")

# Divisão inteira (passa o resultado para int)
div_i = numero1 // numero2
print(f"Divisão inteira dos números: {div_i}")

# Potência
pot = numero1 ** numero2
print(f"Potência dos números: {pot}")

# Módulo (Resto da divisão)
mod = numero1 % numero2
print(f"Resto da divisão dos números: {mod}")
